// Create a variable to store first name
var firstName = "John";

// Create a variable to store last name
var lastName = "Doe";

// Change the value of last name
lastName = "Smith";

// Create a variabel to combie first + last name
var fullName = firstName + " " + lastName;

// Print full name to the console
console.log("Hello " + fullName + ".");



